class Fish {
	
	private String name;
	private int kg;
	private int price;
	
	
	public void setName(String name){
		this.name=name;
		
	}
	public String getName(){
		return name;
	}
	
	public void setKg(int kg){
		this.kg=kg;
	
		
	}
	public int getKg(){
		return kg;
	}
	
	public void setPrice(int price){
		this.price=price;
		
	}
	
	public int getPrice(){
		return price;
	}
	
	


}